CREATE DATABASE MERCADO_DOIS_IRMAOS_DB;
GO
USE MERCADO_DOIS_IRMAOS_DB
GO
CREATE TABLE PRODUTO(
    ID INT IDENTITY(1,1) NOT NULL,
    DESCRICAO VARCHAR(250) NOT NULL,
    VALIDADE DATE NOT NULL,
    VALOR DECIMAL(11,2) NOT NULL,
    QUANTIDADE INT NOT NULL,
    ATIVO BIT NOT NULL
    CONSTRAINT PK_PRODUTO PRIMARY KEY(ID)
);
GO
CREATE TABLE CLIENTE (
    CPF BIGINT NOT NULL,
    NOME VARCHAR(150) NOT NULL,
    DATA_NASCIMENTO DATE NOT NULL,
    PONTOS_FIDELIDADE DECIMAL(11,2) NOT NULL,
    CONSTRAINT PK_CPF PRIMARY KEY (CPF)
);
GO
CREATE TABLE PEDIDO (
    ID_PEDIDO INT IDENTITY(1,1) NOT NULL,
    CPF_CLIENTE BIGINT NULL,
    ID_PRODUTO INT NOT NULL,
    DATA_HORA DATETIME NOT NULL,
    QUANTIDADE_PRODUTO INT NOT NULL,
    STATUS_PEDIDO INT NOT NULL,
    VALOR_TOTAL DECIMAL(11,2) NOT NULL,
    CONSTRAINT PK_PEDIDO PRIMARY KEY (ID_PEDIDO),
    CONSTRAINT FK_CPF_CLIENTE FOREIGN KEY (CPF_CLIENTE)
    REFERENCES CLIENTE(CPF),
    CONSTRAINT FK_ID_PRODUTO FOREIGN KEY (ID_PRODUTO)
    REFERENCES PRODUTO(ID)
);
GO
